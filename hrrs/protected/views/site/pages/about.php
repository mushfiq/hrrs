    Brief Problem Statement
The outsourcing industry in Saudi Arabia is relatively new and relies on traditional business model. Liu (2009) states that traditional enterprise human resources recruitment system is not flexible enough and is independent of each other and does not follow the standards with the lack of communication between each other. The development in Information and Communication Technology had an implication on internet adoption growth rate and especially on web technology use. The evolution of online recruitment industry closely parallels such development worldwide. Nowadays, online recruitment has become an efficient way to provide better interaction and information exchange between job seekers and employers (Kamel, 2006) in most of the developed countries. The purpose of this research is to introduce web based recruitment information System into outsourcing business and implement e-business model particularly, into a outsourcing business aiming reusability and flexibility. The idea of the project derived from a running of a small family outsourcing business whilst the business fully depends on traditional outsourcing model. In this process future direction of the organization will be determined whether the business will adopt e-business model in the future strategic direction or not. The write foresees a possible outcome which is success of e-business model in outsourcing business in Saudi Arabia.

